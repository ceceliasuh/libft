#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*str;
	size_t	i;

	str = dst;
	i = 0;
	while (i < n)
	{
		*(str + i) = *(char *)(src + i);
		i++;
	}
	return (dst);
}
